static const char *marpaESLIFJSON_decode_extended_grammars =
  "                   #######################################################\n"
  "                   # >>>>>>>>>>>>>>> Extended JSON Grammar <<<<<<<<<<<<<<<\n"
  "                   #######################################################\n"
  "\n"
  "# --------------------------------------------------\n"
  "# Default action is to propagate the first RHS value\n"
  "# --------------------------------------------------\n"
  ":desc      ::= 'Extended JSON Grammar'\n"
  ":default   ::= event-action      => marpaESLIFJsonEventAction\n"
  "               fallback-encoding => UTF-8\n"
  "\n"
  "# -----------------------------------------------------------\n"
  "# Discard unsignificant whitespaces, Perl comment C++ comment\n"
  "# -----------------------------------------------------------\n"
  ":discard   ::= /[\\x{9}\\x{A}\\x{D}\\x{20}]+|#[^\\n]*|\\/\\/[^\\n]*|\\/\\*(?:(?:[^\\*]+|\\*(?!\\/))*)\\*\\//u\n"
  "\n"
  "# ----------------\n"
  "# Start is a value\n"
  "# ----------------\n"
  ":start     ::= value\n"
  "\n"
  "# ------\n"
  "# Events\n"
  "# ------\n"
  ":terminal  ::= '['          pause => after  event => 0_inc$\n"
  ":terminal  ::= ']'          pause => after  event => 1_dec$\n"
  ":terminal  ::= '{'          pause => after  event => 0_inc$\n"
  ":terminal  ::= '}'          pause => after  event => 1_dec$\n"
  "\n"
  ":lexeme    ::= CHAR         pause => after  event => 2_CHAR$\n"
  ":lexeme    ::= CNTRL_CHAR   pause => after  event => 3_CNTRL_CHAR$\n"
  ":lexeme    ::= CONSTANT     pause => after  event => 4_CONSTANT$\n"
  ":terminal  ::= '\"'         pause => after  event => 5_DQUOTE$\n"
  ":lexeme    ::= NUMBER       pause => after  event => 6_NUMBER$\n"
  "\n"
  "# ----------\n"
  "# JSON value\n"
  "# ----------\n"
  "value      ::= string\n"
  "             | number\n"
  "             | object\n"
  "             | array\n"
  "             | constant\n"
  "\n"
  "# -----------\n"
  "# JSON String\n"
  "# -----------\n"
  "string     ::= '\"' chars '\"'\n"
  "chars      ::= char*\n"
  "char       ::= CHAR\n"
  "             | CNTRL_CHAR\n"
  "\n"
  "# -----------\n"
  "# JSON Number\n"
  "# -----------\n"
  "number     ::= NUMBER\n"
  "\n"
  "# -----------\n"
  "# JSON object\n"
  "# -----------\n"
  "object     ::= '{' members '}'\n"
  "members    ::= pair*                        separator      => commas # ... separated by comma(s)\n"
  "                                            proper         => 0      # ... with eventual trailing separator\n"
  "pair       ::= string ':' value\n"
  "\n"
  "# ----------\n"
  "# JSON Array\n"
  "# ----------\n"
  "array      ::= '[' elements ']'             \n"
  "elements   ::= value*                      separator      => commas  # ... separated by comma(s)\n"
  "                                           proper         => 0       # ... with eventual trailing separator\n"
  "\n"
  "# -------------\n"
  "# JSON constant\n"
  "# -------------\n"
  "constant   ::= CONSTANT\n"
  "\n"
  "# ---------\n"
  "# Separator\n"
  "# ---------\n"
  "comma      ::= ','\n"
  "commas     ::= comma+\n"
  "\n"
  "# -------\n"
  "# Lexemes\n"
  "# -------\n"
  ":desc        ~ 'Extended JSON Grammar Lexemes'\n"
  ":default     ~ fallback-encoding => UTF-8\n"
  "CHAR         ~ /(?:[^\"\\\\\\x00-\\x1F]+)|(?:\\\\[\"\\\\\\/bfnrt])|(?:(?:\\\\u[[:xdigit:]]{4})+)/\n"
  "CNTRL_CHAR   ~ /[\\x00-\\x1F]/\n"
  "CONSTANT     ~ /true|false|null/i\n"
  "NUMBER       ~ /(?:[+-]?(?:[0-9]+)(?:\\.[0-9]+)?(?:[eE][+-]?[0-9]+)?)|(?:[+-]?[Ii][Nn][Ff](?:[Ii][Nn][Ii][Tt][Yy])?)|(?:[+-]?[Nn][Aa][Nn])/\n"
  ;
